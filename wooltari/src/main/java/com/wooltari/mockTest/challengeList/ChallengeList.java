package com.wooltari.mockTest.challengeList;

public class ChallengeList {
	private int challList;
	private String examinfoName;
	private String questionSubject;
	private String examdate;
	private String examinfoOrgan;
	private int result;
	private String pass;
	private int challNum;
	

	public int getChallNum() {
		return challNum;
	}
	public void setChallNum(int challNum) {
		this.challNum = challNum;
	}
	public int getChallList() {
		return challList;
	}
	public void setChallList(int challList) {
		this.challList = challList;
	}
	public String getExaminfoName() {
		return examinfoName;
	}
	public void setExaminfoName(String examinfoName) {
		this.examinfoName = examinfoName;
	}
	public String getQuestionSubject() {
		return questionSubject;
	}
	public void setQuestionSubject(String questionSubject) {
		this.questionSubject = questionSubject;
	}
	public String getExamdate() {
		return examdate;
	}
	public void setExamdate(String examdate) {
		this.examdate = examdate;
	}
	public String getExaminfoOrgan() {
		return examinfoOrgan;
	}
	public void setExaminfoOrgan(String examinfoOrgan) {
		this.examinfoOrgan = examinfoOrgan;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
}
